use crate::{ext, AtomExt, Buf, BufMut, Encode, Error, Ext, FourCC, Result};

// SampleEncryptionBox (`senc`).
// From ISO/IEC 23007-7:2023 (Common Encryption), Section 7.2.1

// This can't really be parsed here, because it requires Per_sample_IV_Size,
// so unfortunately you just get a vector to work out yourself.

#[derive(Debug, Clone, PartialEq, Eq, Default)]
#[cfg_attr(feature = "serde", derive(serde::Serialize, serde::Deserialize))]
pub enum SencBoxVersion {
    #[default]
    V0,
    V1,
    V2,
}

ext! {
    name: Senc,
    versions: [0, 1, 2],
    flags: {senc_use_subsamples = 1,}
}

#[derive(Debug, Clone, PartialEq, Eq, Default)]
#[cfg_attr(feature = "serde", derive(serde::Serialize, serde::Deserialize))]
pub struct Senc {
    pub version: SencBoxVersion,
    pub use_subsamples: bool,
    pub data: Vec<u8>,
}

impl AtomExt for Senc {
    const KIND_EXT: FourCC = FourCC::new(b"senc");

    type Ext = SencExt;

    fn decode_body_ext<B: Buf>(buf: &mut B, ext: SencExt) -> Result<Self> {
        let bytes_remaining = buf.remaining();
        let bytes = buf.slice(bytes_remaining).to_vec();
        buf.advance(bytes_remaining);
        Ok(Senc {
            version: match ext.version {
                SencVersion::V0 => SencBoxVersion::V0,
                SencVersion::V1 => SencBoxVersion::V1,
                SencVersion::V2 => SencBoxVersion::V2,
            },
            use_subsamples: ext.senc_use_subsamples,
            data: bytes,
        })
    }

    fn encode_body_ext<B: BufMut>(&self, buf: &mut B) -> Result<SencExt> {
        self.data.encode(buf)?;
        Ok(SencExt {
            version: match self.version {
                SencBoxVersion::V0 => SencVersion::V0,
                SencBoxVersion::V1 => SencVersion::V1,
                SencBoxVersion::V2 => SencVersion::V2,
            },
            senc_use_subsamples: self.use_subsamples,
        })
    }
}

