    #[test]
    fn test_senc_v1() {
        // Probably not useful for real encryption. Like CENC.
        let senc = Senc {
            version: SencBoxVersion::V1,
            use_subsamples: false,
            data: vec![0x00, 0x00, 0x00, 0x00, 0x00, 0x00],
        };
        let mut buf = Vec::new();
        senc.encode(&mut buf).unwrap();
        assert_eq!(
            buf,
            vec![
                0x00, 0x00, 0x00, 0x12, 0x73, 0x65, 0x6e, 0x63, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00
            ]
        );

        let decoded = Senc::decode(&mut buf.as_ref()).unwrap();
        assert_eq!(decoded, senc);
    }

